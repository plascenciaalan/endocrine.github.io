                        <h4>Efectos de los Disruptores Endocrinos Artificiales</h4>    
                        <p>La exposición a los disruptores endocrinos artificiales, ya sea a través del contacto directo con productos que los contienen o por su presencia en el medio ambiente (agua, aire, alimentos), puede tener efectos perjudiciales a corto y largo plazo en la salud. Los principales efectos incluyen:</p>                
                        <ul>
                            <li><i class="bi bi-check2-all"></i> <span>Desarrollo anormal del sistema reproductivo: Los disruptores endocrinos pueden interferir con el desarrollo sexual, especialmente en fetos, recién nacidos y niños. Pueden causar malformaciones en los órganos reproductivos y afectar la fertilidad.</span></li>
                            <li><i class="bi bi-check2-all"></i> <span>Cánceres hormonales: Algunos disruptores endocrinos, como el BPA y los parabenos, pueden aumentar el riesgo de cánceres relacionados con hormonas, como el cáncer de mama, próstata y testículos.</span></li>
                            <li><i class="bi bi-check2-all"></i> <span>Alteraciones metabólicas: Los disruptores endocrinos pueden contribuir a la obesidad, diabetes tipo 2 y otros trastornos metabólicos al interferir con la regulación de las hormonas involucradas en el metabolismo, como la insulina y las hormonas tiroideas.</span></li>
                            <li><i class="bi bi-check2-all"></i> <span>Problemas cognitivos y de desarrollo: La exposición a disruptores endocrinos puede afectar el desarrollo cerebral y cognitivo, especialmente en niños, aumentando el riesgo de trastornos como el TDAH (trastorno por déficit de atención e hiperactividad).</span></li>
                        </ul>
